/** Renders icon + label for a stage name string (strips leading emojis automatically) */
export function StageBadge({ stage, className, style }: { stage: string; className?: string; style?: CSSProperties }) {
  // Strip leading emoji + space if present
  const clean = stage.replace(/^[\p{Emoji}\s]+/u, "").trim();
  const icon = STAGE_ICONS[NAME_TO_KEY[clean] ?? ""] ?? null;
  return (
    <span className={`inline-flex items-center gap-1 ${className ?? ""}`} style={style}>
      {icon}
      {clean}
    </span>
  );
}
